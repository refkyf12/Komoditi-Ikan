    public void simpanData(){
        String id_ikan = txt_kode.getText();
        String nama_ikan = txt_nama.getText();
        String jenis_ikan = txt_jenis.getText();
        String spesies_ikan = txt_spesies.getText();
        try{
            Statement stmt = koneksi.createStatement();
            String query = "INSERT INTO data_ikan(id_ikan,nama_ikan,jenis_ikan,spesies_ikan) "
                    + "VALUES('"+id_ikan+"', '"+nama_ikan+"', '"+jenis_ikan+"', '"+spesies_ikan+"')";
            System.out.println(query);
            int berhasil = stmt.executeUpdate(query);
            if(berhasil == 1){
                JOptionPane.showMessageDialog(null, "Data berhasil dimasukkan");
            }else{
                JOptionPane.showMessageDialog(null, "Data gagal dimasukkan");
            }
            
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan pada database");
        }
    }
    public void showData(String id_ikan){
        try{
            Statement stmt = koneksi.createStatement();
            String query = "SELECT * FROM data_ikan WHERE id_ikan ='"+id_ikan+"'";
            ResultSet rs = stmt.executeQuery(query);
            rs.first();
            txt_kode.setText(rs.getString("id_ikan"));
            txt_nama.setText(rs.getString("nama_ikan"));
            txt_jenis.setText(rs.getString("jenis_ikan"));
            txt_spesies.setText(rs.getString("spesies_ikan"));
            
        }catch(SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null,"Terjadi Kesalahan Pada Query");
        }
    }
    
    public void EditData(){
        String id_ikan = txt_kode.getText();
        String nama_ikan = txt_nama.getText();
        String jenis_ikan = txt_jenis.getText();
        String spesies_ikan = txt_spesies.getText();
        
        try{
            Statement stmt = koneksi.createStatement();
            String query = "UPDATE data_ikan SET nama_ikan = '"+nama_ikan+"',"
                            + "jenis_ikan = '"+jenis_ikan+"',"
                            + "spesies_ikan ='"+spesies_ikan+"' WHERE id_ikan = '"+id_ikan+"'";
            
            System.out.println(query);
            int berhasil = stmt.executeUpdate(query);
            if(berhasil == 1){
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            }else{
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            }
        }catch(SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Pada Database");
            }
    }

